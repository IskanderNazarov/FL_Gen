public class ComplexityData {

    public int levelsCount;
    public int complexity;
    public int colorsCount;
    public int sameColorProb_1;
    public int sameColorProb_2;

    public ComplexityData(int complexity, int levelsCount, int colorsCount, int sameColorProb1, int sameColorProb2) {
        this.levelsCount = levelsCount;
        this.complexity = complexity;
        this.colorsCount = colorsCount;
        sameColorProb_1 = sameColorProb1;
        sameColorProb_2 = sameColorProb2;
    }
}
    
        

    